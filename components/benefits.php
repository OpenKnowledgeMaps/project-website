    <h2 class="h2reverse">Advantages</h2>
    <div class="benefits">

        <div class="benefit-point">
            <p class="icon"><img src="./img/benefits-OKM/overview.png" alt='Get an overview of a research topic'>
            </p> 
            <p class="icon-description"><strong>Get an overview of a research topic:</strong> knowledge maps 
                provide an instant overview of a topic by showing the main areas at a glance, 
                and papers related to each area. This makes it possible to easily identify useful, 
                pertinent information.
            </p>
        </div>

        <div class="benefit-point">
            <p class="icon"><img src="./img/benefits-OKM/irrelevant.png" alt='Identify relevant content'>
            </p>
            <p class="icon-description"><strong>Separate the wheat from the chaff:</strong> we cluster similar papers together. 
                This makes it easier to identify relevant content when you are searching for an ambiguous term, 
                or when you would like to identify content from a single discipline in a multidisciplinary field.
            </p>
        </div>

        <div class="benefit-point">
            <p class="icon"><img src="./img/benefits-OKM/concepts.png" alt='Identify relevant concepts'>
            <p class="icon-description">
                <strong>Identify relevant concepts:</strong> one of the most difficult tasks when you are new in a research field 
                is to learn the “language” of the field. Open Knowledge Maps makes it easier for you by 
                labeling research areas with relevant concepts.
            </p>
        </div>

        <div class="benefit-point">
            <p class="icon"><img src="./img/benefits-OKM/open-content.png" alt='Find open content'>
            <p class="icon-description">
                <strong>Find open content:</strong> our knowledge maps include both closed and open access papers. However we highlight open access papers - 
                and the majority of those papers can be read from within the interface. And if not, 
                the fulltext is only a click away.
            </p>
        </div>

