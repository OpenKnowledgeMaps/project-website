                <h3>Open Knowledge Maps Frontend Refactoring</h3>
                <p class="project-facts">With eLife's backing, we are performing a necessary refactoring of the Open Knowledge Maps frontend.

                    The refactoring improves the serviceability, reusability and structure of our frontend. The aim is to make it easier to introduce new functionality crucial to fulfilling important use cases, such as integrating Open Knowledge Maps into user workflows, enabling future integrations (e.g. of dataset indices), and reaching our goal of a collaborative discovery environment.</p>
