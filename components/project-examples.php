                <p class="project-facts">CoVis is an ongoing project in collaboration with ReFigure funded by EOSCSecretariat. CoVis enables scientists to kick-start their COVID-19 research by providing a curated knowledge map of seminal works on COVID-19 from eight critical areas of biomedical research. The knowledge map is constantly evolving thanks to the collective editing of subject-matter experts.</p>
