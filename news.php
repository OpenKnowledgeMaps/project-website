                    <img style="width:100%;" src="https://openknowledgemaps.org/img/newsletter/openairetender.png">
                    <div class="newsitem-text">
                        <time datetime="2018-03-20">20 March 2018</time>
                        <h3 class="newsitem-headline">Open Knowledge Maps wins OpenAIRE tender to create a tool that lets you visualize any research project</h3>
                        <p class="newsitem-p">
                            What do snakes and the visualization of research projects have in common? The answer is: <b>VIPER</b>. VIPER stands for <b>VI</b>sual <b>P</b>roject <b>E</b>xplore<b>R</b>, a unique open science application that focuses on the discovery of research project results. VIPER will enable you to systematically explore a project's output and to understand its impact in different areas.
                        </p>
                        <p class="newsitem-p">
                            VIPER is made possible thanks to <a href="http://openaire.eu" target="_blank">OpenAIRE</a>, a key infrastructure that enables the European transition to open science.
                            At the end of 2017, OpenAIRE launched an open call for tenders, seeking innovative ideas that would improve the OpenAIRE infrastructure services and/or their overall uptake.
                            We are delighted to announce that Open Knowledge Maps's proposal has been among the 5 winning bids.
                        </p>
                        <p class="newsitem-p">
                            To realize this idea, we will create knowledge maps for more than 700.000 projects by 15 funding agencies in OpenAIRE's massive database. VIPER exploits a unique property of OpenAIRE data:
                            the link between projects and publications and datasets. Stay tuned for more infos and updates!
                        </p>
                        <a class="newsitem-button" href="https://www.openaire.eu/openaire-tender-calls-results" target="_blank">
                            OpenAIRE tender call results</a>
